/*10) Crie uma função que verifica se um número inteiro passado como parêmetro é divisível por 3 e retorne true
ou false.*/

function div3(n){
    if(n/3!=!0){
        return true
    }
        return false
    
}

console.log(div3(3))
console.log(div3(4))
console.log(div3(12))
console.log(div3(20))